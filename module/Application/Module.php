namespace Application;

use Zend\ModuleManager\ModuleManager;
use Zend\Mvc\MvcEvent;

/**
 * Application entry point.
 *
 * @category    akandels
 * @package     bullshitcharts
 * @copyright   Copyright (c) 2013 Andrew P. Kandels (http://andrewkandels.com)
 * @license     http://www.opensource.org/licenses/bsd-license.php BSD License
 */
class Module
{
    /**
     * Returns the autoloader configuration to load php source files
     * sans the requires and includes.
     *
     * @return  array
     */
    public function getAutoloaderConfig()
    {
        return array(
            'Zend\Loader\ClassMapAutoloader' => array(
                __DIR__ . '/autoload_classmap.php',
            ),
            'Zend\Loader\StandardAutoloader' => array(
                'namespaces' => array(
                    __NAMESPACE__ => __DIR__ . '/src/' . __NAMESPACE__,
                ),
            ),
        );
    }

    /**
     * Retrieves the configuration from the module's php-based
     * configuration file stored in php.
     *
     * @return  array
     */
    public function getConfig()
    {
        return include __DIR__ . '/config/module.config.php';
    }
}
